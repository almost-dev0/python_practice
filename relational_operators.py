a = 10
b = 20

#checking equality using equalto operator
print(a == b)   #false

#checking equality using not equal to operator
print(a != b)   #true

#greater than
print(a > b)    #false

#greater than equal to
print(a >= b)   #false

#less than 
print(a < b)    #true

#less than equal to
print(a <= b)   #true