a = 4
b = 2

#sum
s = a + b
print("The Sum is : ",s)

#difference
d = a - b
print("The Difference is : ",d)

#product
p = a * b
print("The Product is : ",p)

#remainder of division
r = a % b
print("The Remainder is : ",r)

#quotient of division
q = a / b
print("The Quotient is : ",q)

#power/raised to
pow = a ** b
print("The Power of a to b is :",pow)